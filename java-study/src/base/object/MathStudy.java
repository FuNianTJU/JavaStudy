package base.object;

public class MathStudy {
	public static void main(String args[]){
		System.out.println(Math.E); //数学常量 e
		System.out.println(Math.PI); //圆周率常量
		System.out.println(Math.exp(5.7)); //e的参数次幂
		System.out.println(Math.random()); //产生0~1之间的伪随机数
		System.out.println(Math.sqrt(9)); //开平方
		System.out.println(Math.pow(2,3)); //2的3次幂
		System.out.println(Math.round(99.6)); //四舍五入
		System.out.println(Math.abs(-8.09)); //绝对值
	}
}
